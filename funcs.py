def add_numbers():
    number_1 = input("Введите первое число: ")
    number_2 = input("Введите второе число: ")
    result = number_1 + number_2
    print("Результат сложения:", result)


def subtract_numbers():
    number_1 = input("Введите первое число: ")
    number_2 = input("Введите второе число: ")
    result = number_1 - number_2
    print("Результат сложения:", result)
