def add_numbers():
    a = float(input("Введите первое число: "))
    b = float(input("Введите второе число: "))
    sums = a + b
    print("Результат сложения:", sums)


def subtract_numbers():
    a = float(input("Введите первое число: "))
    b = float(input("Введите второе число: "))
    sumv = a - b
    print("Результат вычитания:", sumv)
